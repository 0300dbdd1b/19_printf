#include "libft.h"

int	ft_len_nbr(int nbr, int base)
{
	long int	n;
	int			i;

	n = nbr;
	i = 0;
	if (n < 0)
	{
		i = 1;
		n = n * -1;
	}
	while (n >= base)
	{
		n = n / base;
		i++;
	}
	return (i + 1);
}
